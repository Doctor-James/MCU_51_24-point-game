//左屏幕显示
void display_32_left(unsigned char x,unsigned char y,unsigned char number[32])
{
	unsigned char loop;
	set_adr(x,y);
	for(loop = 0;loop<16;loop++)
	{
		write_data(number[loop],1,0);
	}
	set_adr(x+1,y);
	for(loop = 16;loop<32;loop++)
	{
		write_data(number[loop],1,0);
	}
}

//右屏幕显示
void display_32_right(unsigned char x,unsigned char y,unsigned char number[32])
{
	unsigned char loop;
	set_adr(x,y);
	for(loop = 0;loop<16;loop++)
	{
		write_data(number[loop],0,1);
	}
	set_adr(x+1,y);
	for(loop = 16;loop<32;loop++)
	{
		write_data(number[loop],0,1);
	}
}



//写指令
void write_code(unsigned char c,unsigned char cs1, unsigned char cs2)
{
	LCD_CS1 = cs1;
	LCD_CS2 = cs2;
	LCD_RS = 0;
	LCD_RW = 0;
	P2 = c;
	LCD_E = 0;
	delay(10);
	LCD_E = 1;
	delay(10);
	LCD_E = 0;
}

//写数据
void write_data(unsigned char c,unsigned char cs1,unsigned char cs2)
{
	LCD_CS1 = cs1;
	LCD_CS2 = cs2;
	LCD_RW = 0;
	LCD_RS = 1;
	P2 = c;
	LCD_E = 0;
	delay(10);
	LCD_E = 1;
	delay(10);
	LCD_E = 0;
}


void set_stratline(unsigned char line)
{
	line = 0xc0+line;
	write_code(line,1,1);	//最底层作为第一层
}


void set_adr(unsigned char x,unsigned char y)
{
	x = x+0xb8;
	y = y+0x40;
	write_code(x,1,1);
	write_code(y,1,1);
}

void dis_on_off(unsigned char commnd)
{
	commnd = commnd+0x3e;
	write_code(commnd,1,1);
}

void clear()
{
	unsigned char i,j;
	for(i = 0;i<8;i++)
	{
		set_adr(i,0);
		for(j = 0;j<64;j++)
		{
			write_data(0x00,1,1);
		}
	}
}







